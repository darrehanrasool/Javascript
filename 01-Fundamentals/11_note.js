/************************************************************
 * JAVASCRIPT slice() – COMPLETE UNDERSTANDING CHEAT SHEET
 * slice(start, end)
 * ✅ start → INCLUDED
 * ❌ end   → EXCLUDED
 * ❌ original array/string is NOT modified
 * its simply the upper bound - lower bound
 ************************************************************/
let arr = ["A", "B", "C", "D", "E"];
let str = "JavaScript";

console.log("Original Array:", arr);
console.log("Original String:", str);

/*----------------------------------------------------------
 1️⃣ slice() with NO arguments → full copy
----------------------------------------------------------*/
console.log(arr.slice());
// ["A", "B", "C", "D", "E"]

/*----------------------------------------------------------
 2️⃣ slice(start) → from start index to end
----------------------------------------------------------*/
console.log(arr.slice(2));
// ["C", "D", "E"]

/*----------------------------------------------------------
 3️⃣ slice(start, end) → end is EXCLUDED
----------------------------------------------------------*/
console.log(arr.slice(1, 4));
// ["B", "C", "D"]

/*----------------------------------------------------------
 4️⃣ slice(0, 1) → single element
----------------------------------------------------------*/
console.log(arr.slice(0, 1));
// ["A"]

/*----------------------------------------------------------
 5️⃣ slice(0, arr.length) → full array
----------------------------------------------------------*/
console.log(arr.slice(0, arr.length));
// ["A", "B", "C", "D", "E"]

/*----------------------------------------------------------
 6️⃣ slice(-n) → last n elements
----------------------------------------------------------*/
console.log(arr.slice(-2));
// ["D", "E"]

/*----------------------------------------------------------
 7️⃣ slice(start, -end)
----------------------------------------------------------*/
console.log(arr.slice(1, -1));
// ["B", "C", "D"]

/*----------------------------------------------------------
 8️⃣ slice(-start, -end)
----------------------------------------------------------*/
console.log(arr.slice(-4, -1));
// ["B", "C", "D"]

/*----------------------------------------------------------
 9️⃣ start >= array length → empty array
----------------------------------------------------------*/
console.log(arr.slice(10));
// []

/*----------------------------------------------------------
 🔟 start > end → empty array
----------------------------------------------------------*/
console.log(arr.slice(3, 1));
// []

/*----------------------------------------------------------
 1️⃣1️⃣ negative start too large → treated as 0
----------------------------------------------------------*/
console.log(arr.slice(-100, 2));
// ["A", "B"]

/*----------------------------------------------------------
 1️⃣2️⃣ slice() DOES NOT modify original array
----------------------------------------------------------*/
let newArr = arr.slice(1, 3);
console.log("New Array:", newArr);
console.log("Original Array After slice:", arr);

/*==========================================================
 STRING SLICE (same rules)
==========================================================*/

/*----------------------------------------------------------
 1️⃣3️⃣ Basic string slice
----------------------------------------------------------*/
console.log(str.slice(0, 4));
// "Java"

/*----------------------------------------------------------
 1️⃣4️⃣ slice(start) on string
----------------------------------------------------------*/
console.log(str.slice(4));
// "Script"

/*----------------------------------------------------------
 1️⃣5️⃣ slice with negative index (string)
----------------------------------------------------------*/
console.log(str.slice(-6));
// "Script"

/*----------------------------------------------------------
 1️⃣6️⃣ negative start & end (string)
----------------------------------------------------------*/
console.log(str.slice(-10, -6));
// "Java"

/************************************************************
 🔑 MEMORY RULES
 -----------------------------------------------------------
 ✔ slice(start, end)
 ✔ start INCLUDED
 ✔ end EXCLUDED
 ✔ result length = end - start
 ✔ safe method (no mutation)
 ************************************************************/
